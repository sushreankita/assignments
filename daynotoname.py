n=int(input('enter day no'))
if n == 0:
    print('monday')
elif n == 1:
    print('tuesday')
elif n == 2:
    print('wednesday')
elif n == 3:
    print('thursday')
elif n == 4:
    print('friday')
elif  n == 5:
    print('saturday')
elif n == 6:
    print('sunday')
else:
    print('invalid day no')